				<p>Kidney disease is a silent enemy that can attack anyone.  No age is immune from this sickness, and can drastically affect one's life-style. Consult your Doctor if any of these symptoms occur.  Kidney disease leads to kidney failure which is life threatening.  Special therapy is required e.g. dialysis of which there are two types, namely Haemodialysis and Peritoneal dialysis. Kidney transplantation however is still the best form of therapy.</p>
				<p>Chronic Kidney Disease is another common, life-threatening illness that often goes undetected until very advanced when it could be diagnosed early through simple tests. Today's epidemics of diabetes and obesity could contribute to even higher rates of Chronic Kidney Disease in the future.  Undiagnosed and untreated chronic kidney disease can lead to serious health problems including kidney failure.  Diagnosed early, it can be managed, and kidney damage can be slowed or stopped, that is why testing for people at risk is so important.  If you have diabetes, high blood pressure or a family history of kidney disease, you're at risk and should request your doctor about the simple tests for kidney function.</p>
				
