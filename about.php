				<h3>Our Company</h3>
				<p> The Cape Kidney Association (CKA) is a grassroots non-profit organization which was established in 1978 and is involved in raising awareness about maintaining kidney health and preventing kidney failure. In addition the CKA is involved in fundraising efforts to assist indigent patients with chronic kidney failure on dialysis. The CKA provides the indigent patients with nutritious food parcels, study bursaries and assist their entrepreneurship projects so that they can sustain themselves financially and regain their self-esteem.</p>
				<p></p>
				
                                <h3>Company Goals</h3>
				<p>We supply food parcels to indigent patients on a monthly basis to Provincial Hospital, and we have now extended the distribution to renal units in surrounding areas of the Western Cape. We provide families with financial support to start up small businesses. Due to our Indigent Kidney patients having to be on dialysis three (3) times per week for four (4) hours, they are not always able to maintain their jobs, therefore we assist them to become self-employed. We provide bursaries for patients who wish to continue their studies.  Patients, due to lack of funds within the families, are assisted in order to find sustainable jobs.
                                    We provide funds to the Social Workers at the Provincial Hospitals. These funds are used to assist the patients with transport costs to get to the hospitals for their dialysis and to collect medication which they require daily. We embark on public education by means of exhibitions in shopping centers as well as distribution of pamphlets and brochures.  We also present talks on 'Caring for your Kidneys" at Corporate Companies, social groups and schools on request, as well as arranging 'Wellness Days'.</p>
				
